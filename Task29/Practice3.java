/**
 * Практика #3 - Пример использования аннотации @Override для обнаружения ошибок
 *
 * Ответ на вопрос: Аннотация @Override помогает обнаружить ошибки на этапе компиляции, когда:
 * - Опечатка в имени метода
 * - Несоответствие сигнатуры метода (разные параметры)
 * - Попытка переопределить несуществующий метод в родительском классе
 * Без @Override такие ошибки останутся незамеченными и метод будет считаться новым, а не переопределением
 *
 * @author Андрей Иванов
 * @version 1.0
 * @since 2025
 */
public class Practice3 {

    /**
     * Базовый класс кнопки
     */
    static class Button {

        /**
         * Метод отрисовки кнопки
         */
        public void render() {
            System.out.println("Отрисовка базовой кнопки");
        }

        /**
         * Метод клика по кнопке
         */
        public void onClick() {
            System.out.println("Клик по кнопке");
        }
    }

    /**
     * Подкласс кнопки отправки
     */
    static class SubmitButton extends Button {

        /**
         * Правильное переопределение метода render
         * Аннотация @Override подтверждает, что метод существует в родительском классе
         */
        @Override
        public void render() {
            System.out.println("Отрисовка кнопки отправки с зеленым цветом");
        }

        /**
         * Пример ошибки: опечатка в имени метода (onClik вместо onClick)
         * С аннотацией @Override компилятор выдаст ошибку
         */
        // @Override
        // public void onClik() { // Ошибка компиляции: метод не найден в родительском классе
        //     System.out.println("Клик по кнопке отправки");
        // }

        /**
         * Пример ошибки: несоответствие параметров
         * С аннотацией @Override компилятор выдаст ошибку
         */
        // @Override
        // public void onClick(String param) { // Ошибка компиляции: разные сигнатуры методов
        //     System.out.println("Клик с параметром: " + param);
        // }
    }

    /**
     * Главный метод программы
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        SubmitButton submitBtn = new SubmitButton();
        submitBtn.render();
        submitBtn.onClick();
    }
}