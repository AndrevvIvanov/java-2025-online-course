public class Practice1 {

    /**
     * Главный метод программы, демонстрирующий преобразование
     * конструкции if-else в тернарный оператор
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        int a = 2; // Пример значения для тестирования
        int i;

        // Исходный код с if-else
        if (a == 1) {
            i = 1;
        } else if (a == 2) {
            i = 2;
        } else {
            i = 3;
        }

        System.out.println("Результат с if-else: i = " + i);

        // Преобразование в тернарный оператор
        i = (a == 1) ? 1 : (a == 2) ? 2 : 3;

        System.out.println("Результат с тернарным оператором: i = " + i);
    }
}